/** Get nationality by ISO country code. */
export function getNationalityByCode(code: string): NationalityOption | undefined {
  const c = String(code || "").trim().toUpperCase();
  if (!c) return undefined;
  return NATIONALITIES.find((n) => n.countryCode === c || n.code === c);
}

/** Get nationality by demonym (e.g. "Bangladeshi"). */
export function getNationalityByDemonym(demonym: string): NationalityOption | undefined {
  const d = String(demonym || "").trim().toLowerCase();
  if (!d) return undefined;
  return NATIONALITIES.find((n) => n.demonym.toLowerCase() === d);
}

/** Get nationality by country name. */
export function getNationalityByLabel(label: string): NationalityOption | undefined {
  const l = String(label || "").trim().toLowerCase();
  if (!l) return undefined;
  return NATIONALITIES.find(
    (n) => n.label.toLowerCase() === l || n.demonym.toLowerCase() === l
  );
}

/**
 * Nationalities dataset for Owner KYC.
 * Based on ISO 3166-1 alpha-2 country codes with demonyms.
 * Reuses country dataset concept from location system (lib/location/countries, geo.data).
 */

export interface NationalityOption {
  code: string;
  countryCode: string;
  label: string;
  demonym: string;
}

function build(
  code: string,
  name: string,
  demonym?: string
): NationalityOption {
  return {
    code,
    countryCode: code,
    label: name,
    demonym: demonym ?? `${name} national`,
  };
}

